   function somma(a, b) {
     return a + b
   }

   function differenza(a, b) {
     return a - b
   }

   function prodotto(a, b) {
     return a * b
   }

   function dividendo(a, b) {
     return a / b
   }

   const pow = function (a,b){
     return Math.pow(a,b)
   }

   export default {
     somma,
     differenza,
     prodotto,
     dividendo,
     pow
   }